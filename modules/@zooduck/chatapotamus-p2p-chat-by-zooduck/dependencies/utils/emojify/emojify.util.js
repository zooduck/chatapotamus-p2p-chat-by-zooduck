const SINGLE_UNICODE_EMOJI_REGEX = new RegExp('^(' + Object.values(emojiHexMap).map((hex) => {
  return `\\u{${hex}}`;
}).join('|') + ')$', 'u');
const UNICODE_EMOJI_REGEX = new RegExp(Object.values(emojiHexMap).map((hex) => {
  return `\\u{${hex}}`;
}).join('|'), 'gu');
const emojiNameToHTMLEntityMap = Object.freeze(Object.fromEntries(Array.from(Object.entries(emojiHexMap)).map(([key, value]) => {
  return [':' + key.toLowerCase() + ':', '&#x' + value + ';'];
})));
const emojiUnicodeSymbolToNameMap = Object.freeze(Object.fromEntries(Object.entries(emojiHexMap).map(([key, value]) => {
  return [`\\u{${value}}`, ':' + key.toLowerCase() + ':'];
})));
const RANDOM_EMOJI_ALIAS = ':random:';
const EMOJI_REGEX = new RegExp(Object.keys(emojiNameToHTMLEntityMap).join('|') + `|${RANDOM_EMOJI_ALIAS}`, 'g');
const emojify = (string) => {
  return string.replace(EMOJI_REGEX, (match) => {
    const emojiName = match;
    if (emojiName === RANDOM_EMOJI_ALIAS) {
      return getRandomEmoji();
    }
    if (emojiName in emojiNameToHTMLEntityMap) {
      return emojiNameToHTMLEntityMap[emojiName];
    }
    return emojiName;
  });
};
const getEmojiAsHTMLEntity = (emojiName) => {
  return emojiNameToHTMLEntityMap[emojiName] || emojiName;
};
const getEmojiNameFromRenderedEmoji = (renderedUnicodeSymbol) => {
  for (const unicodeSymbol of Object.keys(emojiUnicodeSymbolToNameMap)) {
    if (new RegExp(unicodeSymbol, 'u').test(renderedUnicodeSymbol)) {
      return emojiUnicodeSymbolToNameMap[unicodeSymbol];
    }
  }
  return null;
};
const getRandomEmoji = () => {
  const randomKey = Math.floor(Math.random() * (Object.keys(emojiNameToHTMLEntityMap).length - 1));
  return emojiNameToHTMLEntityMap[Object.keys(emojiNameToHTMLEntityMap)[randomKey]];
};
const emojiAliasDictionary = Object.freeze(Object.fromEntries(Array.from(Object.entries(emojiHexMap)).map(([key, value]) => {
  return [':' + key.toLowerCase() + ':', {
    htmlEntity: '&#x' + value + ';',
    unicodeSymbol: value
  }];
})));
